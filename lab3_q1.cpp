//library
#include<iostream>
using namespace std;
int main()
{
	//declaring variables
	int a;
	char b;
	float c;
	bool d;
	double e;
	//printing variable names and sizes
	cout<< "Integer Datatype"<<a<<sizeof(int)<<endl;
	cout<<"Character Datatype"<<b<<sizeof(char)<<endl;
	cout<< "Float Datatype"<<c<<sizeof(float)<<endl;
	cout<< "Boolean Value"<<d<<sizeof(bool)<<endl;
	cout<< "Double Datatype"<<e<<sizeof(double)<<endl;
	//Terminate the program
	return 0;
}
